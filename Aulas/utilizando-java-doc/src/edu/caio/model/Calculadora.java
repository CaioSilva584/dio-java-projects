public class Calculadora {

    /**
     * Este método recebe dois números inteiros e retorna a soma deles
     * @param numeroUm este é o primeiro número inteiro de parâmetro
     * @param numeroDois este é o segundo número inteiro de parâmetro
     * @return int o resultado da soma dos dois parêmetros
     */

    public int somar(int numeroUm, int numeroDois){
        return numeroUm + numeroDois;
    }
}
